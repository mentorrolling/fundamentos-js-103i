//Switch

//condicional múltiple

let numero = 20;
switch (numero) {
  case 10:
    console.log("El número es 10");
    break;
  case 20:
    console.log("El número es 20");
    break;
  case 50:
    console.log("El número es 50");
    break;
  default:
    //acciones
    console.log("No coincide con ninguna opción");

    break;
}
